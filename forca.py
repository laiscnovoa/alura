import random


def jogar():
    print("*********************************")
    print("***Bem vindo ao jogo da Forca!***")
    print("*********************************")


    palavra_chave = palavra_secreta()
    letras = ['_' for letra in palavra_chave]
    print(letras)

    acerto = False
    forca = False
    erros = 0

    while not acerto and not forca:

        chute = input('Que letra quer testar? ').strip().upper()

        if chute in palavra_chave:
            index = 0
            for letra in palavra_chave:
                if chute.upper() == letra.upper():
                    letras[index] = letra
                index += 1
        else:
            erros += 1
            desenha_forca(erros)

        forca = erros == 7
        acerto = '_' not in letras
        print(letras)

    if acerto:
        mensagem_vencedor()
    else:
        mensagem_perdedor(palavra_chave)

    print('Fim do Jogo')
    
    
def palavra_secreta():
    arquivo = open("frutas.txt", "r")
    palavras = []

    for linha in arquivo:
        linha = linha.strip()
        palavras.append(linha)

    arquivo.close()

    numero = random.randrange(0, len(palavras))
    palavra_secreta = palavras[numero].upper()

    return palavra_secreta


